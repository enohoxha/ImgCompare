package com.diff.images.Models;

public class HashFunctionModel {

    private int a;
    private int b;
    private int c;

    private int x;


    public HashFunctionModel(int a, int b, int c) {
        this.a = a;
        this.b = b;
        this.c = c;
    }

    public int getA() {
        return a;
    }

    public void setA(int a) {
        this.a = a;
    }

    public int getB() {
        return b;
    }

    public void setB(int b) {
        this.b = b;
    }

    public int getC() {
        return c;
    }

    public void setC(int c) {
        this.c = c;
    }

    public int getX() {
        return x;
    }

    public void setX(int x) {
        this.x = x;
    }

    public int calculateHashFunction(){

        return (a * x + b) % c;
    }
}
